# public methods allow interaction with an obj
# public methods can be called by any other obj
# private methods can only be called within the obj
# private methods can not be invoked by another obj
# private methods cannot be called with an explicit receiver -- only the current obj is able to receive the method

# protected methods can be invoked only by obj of the same class
# protected methods can be called within the same family of obj
# protected are user to compare objs of the same class
